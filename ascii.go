var digitASCII = map[rune]string{
	'0': numberZero,
	'1': numberOne,
	'2': numberTwo,
	'3': numberThree,
	'4': numberFour,
	'5': numberFive,
	'6': numberSix,
	'7': numberSeven,
	'8': numberEight,
	'9': numberNine,
	'-': dash,
}

// teamArt returns the ASCII banner for a FIFA code or full team name.
func teamArt(code string) string {
	art, ok := teamASCII[strings.ToUpper(code)]
	if !ok {
		return ""
	}
	return strings.TrimLeft(art, "\n")
}

// scoreArt renders a football score (e.g. "2-1") using block digit art.
func scoreArt(score string) string {
	var parts []string
	for _, ch := range score {
		art, ok := digitASCII[ch]
		if !ok {
			continue
		}
		parts = append(parts, strings.TrimPrefix(art, "\n"))
	}
	if len(parts) == 0 {
		return ""
	}
	return lipgloss.JoinHorizontal(lipgloss.Top, parts...)
}
